package com.idat.idatSindyQuispe.dto;

public class UsuarioClienteResponseDTO {
	private Integer idUsuarioResponse;
	private String usuarioResponse;
	private String passwordResponse;
	private String rolResponse;
	private Integer idClienteResponse;
	
	public Integer getIdUsuarioResponse() {
		return idUsuarioResponse;
	}
	public void setIdUsuarioResponse(Integer idUsuarioResponse) {
		this.idUsuarioResponse = idUsuarioResponse;
	}
	public String getUsuarioResponse() {
		return usuarioResponse;
	}
	public void setUsuarioResponse(String usuarioResponse) {
		this.usuarioResponse = usuarioResponse;
	}
	public String getPasswordResponse() {
		return passwordResponse;
	}
	public void setPasswordResponse(String passwordResponse) {
		this.passwordResponse = passwordResponse;
	}
	public String getRolResponse() {
		return rolResponse;
	}
	public void setRolResponse(String rolResponse) {
		this.rolResponse = rolResponse;
	}
	public Integer getIdClienteResponse() {
		return idClienteResponse;
	}
	public void setIdClienteResponse(Integer idClienteResponse) {
		this.idClienteResponse = idClienteResponse;
	}
	
}
