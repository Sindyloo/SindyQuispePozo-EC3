package com.idat.idatSindyQuispe.dto;

public class HospitalResponseDTO {
	private Integer idHospitalResponse;
	private String nombreResponse;
	private String descripcionResponse;
	private String distritoResponse;
	
	public Integer getIdHospitalResponse() {
		return idHospitalResponse;
	}
	public void setIdHospitalResponse(Integer idHospitalResponse) {
		this.idHospitalResponse = idHospitalResponse;
	}
	public String getNombreResponse() {
		return nombreResponse;
	}
	public void setNombreResponse(String nombreResponse) {
		this.nombreResponse = nombreResponse;
	}
	public String getDescripcionResponse() {
		return descripcionResponse;
	}
	public void setDescripcionResponse(String descripcionResponse) {
		this.descripcionResponse = descripcionResponse;
	}
	public String getDistritoResponse() {
		return distritoResponse;
	}
	public void setDistritoResponse(String distritoResponse) {
		this.distritoResponse = distritoResponse;
	}

}
